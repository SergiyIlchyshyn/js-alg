function writeAlgoDemo() {
    let side_a = Math.random() * 10 + 5;
    let side_b = Math.random() * 10 + 5;
    document.write("Side a = " + side_a + "Side_b = " + side_b);
    document.write("Об'єм куба = " + side_a * side_a * side_a);
    document.write("Площа квадрата = " + side_a * side_a);
    document.write("Площа прямокутника = " + side_a * side_b);
}